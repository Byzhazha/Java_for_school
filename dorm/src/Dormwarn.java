public class Dormwarn {
    String id;//警示号
    String content;//内容

    public Dormwarn() {
    }

    public Dormwarn(String id, String content) {
        this.id = id;
        this.content = content;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }
}
